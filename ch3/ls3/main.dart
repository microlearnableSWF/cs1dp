// Conditional Statement - else if

void main() {
	// Declare a variable "n"
  
	// Print if variable "n" is Less Than 10
  print('This number is less than 10.');

	// Print if variable "n" is Less Than or Equal to 50
  print('This number is less than or equal to 50.');

	// Print if all of the above conditions are false
  print('This number is greater than 50.');
}