// Type Casting

void main() {
  // [String] into [int]
  // Set intNum, strInt

  // Cast strInt Type as Integer
  print(50 + intNum);

  // [int] into [String]

  // Cast 1234 to '1234'
  print(strInt);

  // [String] into [double]
  // Set doubleNum, strDouble

  // Cast strDouble Type as double
  print(doubleNum);

  // [bool] into [String]

  // Cast boolType to String
  print(boolType);
}
